package Topics.Operators;

public class Shifted_Operators {

	public static void main(String[] args) {
		// two types
		/*
		 * 1.Left shifted 2.Right Shifted
		 */
		/*
		 * Left shifted operator what ever the value given of left side that will
		 * multiplies by 2* that num
		 */
		System.out.println(8 << 3);// 10*2*2*2
		
		//Right Shifted Operator
		
		System.out.println(10>>3);//10/2*2*2
	}

}
