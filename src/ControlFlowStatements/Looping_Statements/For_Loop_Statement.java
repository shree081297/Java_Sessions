package ControlFlowStatements.Looping_Statements;

public class For_Loop_Statement {

	public static void main(String[] args) {
		//syntax
		/*for(int a=0;a<10;a++){
		 * statement 
		 */
		System.out.println("levitica");
		System.out.println("levitica");

		/*initial value of levitica is 0
		 *  when a value will become 100
		 */
	for(int a=0;a<100;a++) {
		System.out.println(a + " "+"Levitica");
	}
	}

}
